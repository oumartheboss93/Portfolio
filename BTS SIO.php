<section id="BTS Service informatiques aux organisations">
<h3> BTS Service informatiques aux organisations.</h3> 
<h2>D’abord le Brevet de Technicien Supérieur Services informatique aux Organisations (SIO) s’adresse à ceux qui souhaitent se former en deux ans aux métiers d’administrateur réseau ou de développeur pour intégrer directement le marché du travail ou continuer des études dans le domaine de l’informatique. Mais en plus depuis la nouvelle réforme, la matière de Cybersécurité est disponible. </h2>
</section>
<section id="OPTION SLAM">
<h1>OPTION SLAM</h1>
<img src="https://aymeric-cucherousset.fr/wp-content/uploads/2021/10/bts-sio-slam-768x512.jpg" height="200" width="200"> <br> </br>
<h2>L’acronyme SLAM signifie <b><mark>«Solutions Logicielles et Applications Métier ».</b></mark> 
<br>L’option SLAM est principalement destinée aux étudiants qui s’orientent vers les métiers liés à la conception et la maintenance de programmes applicatifs. Donc grâce à des cours spécifiques, les diplômés seront capables de gérer un parc informatique ou d’administrer un réseau au sein d’une entreprise. Aussi ils pourront également gérer l’intégration, la sécurisation et la configuration des serveurs, mais aussi des postes clients et des équipements d’interconnexion.
<br>Voici quelques exemples de métiers que les étudiants peuvent exercer après le BTS SIO option SLAM :
<br><br>
<li>1.Développeur d’applications informatiques</li>
<li>2.Chargé d’études informatiques</li>
<li>3.Responsable des services applicatifs</li>
<li>4.Programmeur analyste</li>
<li>5.Programmeur d’applications</li>
<li>6.Technicien d’études informatiques</li>
</br></br></h2>
</section>
<section id="OPTION SISR ">
<h1> OPTION SISR </h1>
<img src="https://aymeric-cucherousset.fr/wp-content/uploads/2021/10/bts-sio-sisr.jpeg">
<h2>L’acronyme SISR signifie <b><mark> «Solutions d’infrastructure, systèmes et réseaux ».</b></mark>
<br>L’option SISR est donc d’abord destinée aux étudiants qui s’orientent vers les métiers liés à la conception et la maintenance d’infrastructures réseaux. Avant tout cela correspond à assurer la sécurité, la maintenance et l’installation des réseaux et des équipements informatiques font partie des principales missions des futurs administrateurs, techniciens ou pilotes d’exploitation.
Donc voici quelques exemples de métiers que les étudiants peuvent exercer après le BTS SIO option SISR :
<br><br>
<li>1.Technicien réseau-télécoms</li>
<li>2.Technicien systèmes et réseaux</li>
<li>3.Administrateur systèmes et réseaux</li>
<li>4.Support systèmes et réseaux</li>
<li>5.Technicien d’infrastructure</li>
<li>6.Pilote d’exploitation</li>
<li>7.Technicien de production</li>
</br></br><h2>
</section>
